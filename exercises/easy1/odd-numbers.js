let number = 1;

while (number < 100) {
  console.log(number);
  number += 2;
}

/* Log all odd numbers from 1 to 99 to the console
   The program use `while` loop to iterate over odd numbers starting from 1.
   First, a global variable `number` is declared and initialized to 1.
   Then, we start the loop at 1 and incrementing it by 2 after every iteration.
   This loop would end once the number reaches the limit (100).
*/